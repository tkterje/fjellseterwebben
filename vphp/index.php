            <h2>LANG TRADISJON</h2>
            <p>Løpet fra Lerkendal til Skistua har opprinnelig navnet Sigmonds Staup. Dette løpet har vært en tradisjon blant studentene i en årrekke, og blir fremdeles i en uformell form holdt i etterkant av NTNUIs generalforsamlinger. I 1987 ble Fjellseterløpet etablert som en oppfølger til det originale Staup, men med start ved Marienborg på vestsida av Nidelva. Distansen på 8,1 km var imidlertid den samme. Etter at Stavnebrua ble utvidet ble startstedet i 1996 flyttet til Lerkendal, på østsida av Nidelva, slik det er for Staup.</p>
